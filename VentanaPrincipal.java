    private void botonAtacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAtacarActionPerformed
        /*
        Botón ataque, este comprueba que ambos combatientes esté vivo, de ser así tira dados para comprobar: si el jugador acierta
        y que tipo de ataque obtendrán el jugador y enemigo: Flojo, normal o crítico. Se realiza el daño y se actualizan los labels, en caso 
        de que el enemigo sobreviva el ataque de jugador, este ataca, de no hacerlo, el combate termina.
         */

        if (jug.getPuntosSalud() > 0 && en.getPuntosSalud() > 0) {
            int dadoAcierto = (int) (Math.random() * (6 - 1) + 1) + 1;
            int dadoJug = (int) (Math.random() * (6 - 1) + 1) + 1;
            int dadoEn = (int) (Math.random() * (6 - 1) + 1) + 1;

            // Turno de ataque del jugador
            // Se comprueba si se acierta el ataque con la tirada de dado
            if (dadoAcierto < 5) {
                if (dadoJug < 3) {
                    acciones1.setText("Has atacado al enemigo y le has hecho: " + (jug.getPuntosAtaque() - 1) + " puntos de daño");
                    en.setPuntosSalud(en.getPuntosSalud() - (jug.getPuntosAtaque() - 1));
                } else if (dadoJug > 5) {
                    acciones1.setText("Has atacado al enemigo y le has hecho: " + (jug.getPuntosAtaque() + 1) + " puntos de daño");
                    en.setPuntosSalud(en.getPuntosSalud() - (jug.getPuntosAtaque() + 1));
                } else {
                    acciones1.setText("Has atacado al enemigo y le has hecho: " + jug.getPuntosAtaque() + " puntos de daño");
                    en.setPuntosSalud(en.getPuntosSalud() - jug.getPuntosAtaque());
                }

            } else {
                acciones1.setText("El enemigo ha esquivado tu ataque!");
            }

            vidaEn.setText(en.getPuntosSalud() + "");

            //Turno enemigo
            /*
        Este calcula el daño que realiza en la vida del jugador, según dado y la defensa del jugador.
             */
            if (en.getPuntosSalud() <= 0) {
                acciones1.setText("Has ganado!");
                acciones2.setText("");
                avisoTerminar.setText("Has derrotado al enemigo! Pulsa 'Terminar'");
                en.setPuntosSalud(0);
            } else if (en.getPuntosSalud() > 0) {
                int ajuste;
                if (dadoEn < 2) {
                    if (jug.getDefensa() > en.getPuntosAtaque()) {
                        acciones2.setText("El enemigo no te hace daño!");
                    } else if (jug.getDefensa() > 0) {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + (en.getPuntosAtaque() - 1) + " puntos de daño.");
                        ajuste = (en.getPuntosAtaque() - 1) - jug.getDefensa();
                        jug.setPuntosSalud(jug.getPuntosSalud() - ajuste);
                    } else {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + (en.getPuntosAtaque() - 1) + " puntos de daño.");
                        jug.setPuntosSalud(jug.getPuntosSalud() - (en.getPuntosAtaque() - 1));
                    }

                } else if (dadoEn > 4) {
                    if (jug.getDefensa() > en.getPuntosAtaque()) {
                        acciones2.setText("El enemigo no te hace daño!");
                    } else if (jug.getDefensa() > 0) {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + (en.getPuntosAtaque() + 1) + " puntos de daño.");
                        ajuste = (en.getPuntosAtaque() + 1) - jug.getDefensa();
                        jug.setPuntosSalud(jug.getPuntosSalud() - ajuste);
                    } else {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + (en.getPuntosAtaque() + 1) + " puntos de daño.");
                        jug.setPuntosSalud(jug.getPuntosSalud() - (en.getPuntosAtaque() + 1));

                    }

                } else {
                    if (jug.getDefensa() > en.getPuntosAtaque()) {
                        acciones2.setText("El enemigo no te hace daño!");
                    } else if (jug.getDefensa() > 0) {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + en.getPuntosAtaque() + " puntos de daño.");
                        ajuste = en.getPuntosAtaque() - jug.getDefensa();
                        jug.setPuntosSalud(jug.getPuntosSalud() - ajuste);

                    } else {
                        acciones2.setText("El enemigo te ha atacado haciendo: " + en.getPuntosAtaque() + " puntos de daño.");
                        jug.setPuntosSalud(jug.getPuntosSalud() - en.getPuntosAtaque());
                    }
                }

            }

            if (jug.getPuntosSalud() <= 0) {
                jug.setPuntosSalud(0);
                acciones1.setText("Has perdido :(");
                acciones2.setText("");
                avisoTerminar.setText("Ya has sido derrotado. Pulsa 'Terminar'");
                vidaCombJug.setText(jug.getPuntosSalud() + "/" + jug.getVidaMaxima());
            }

        } else if (jug.getPuntosSalud() <= 0) {
            acciones1.setText("Has perdido :(");
            acciones2.setText("");
            avisoTerminar.setText("Ya has sido derrotado. Pulsa 'Terminar'");
        }

        vidaCombJug.setText(jug.getPuntosSalud() + "/" + jug.getVidaMaxima());
    }//GEN-LAST:event_botonAtacarActionPerformed

    private void botonCombatePocionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCombatePocionActionPerformed
        /*
        Botón usar poción en combate, este comprueba que ambos estén vivos y de estarlo, comprueba si el jugador tiene pociones, de tenerlas
        cura 4 puntos de salud.
         */

        if (jug.getPuntosSalud() > 0 && en.getPuntosSalud() > 0) {
            if (inv.objetos.containsKey("Pocion")) {
                acciones1.setText("Has usado: Pocion. Recuperas 4 puntos de salud.");
                acciones2.setText("");
                jug.setPuntosSalud(jug.getPuntosSalud() + 4);
                if (jug.getPuntosSalud() >= jug.getVidaMaxima()) {
                    jug.setPuntosSalud(jug.getVidaMaxima());
                }
                vidaCombJug.setText(jug.getPuntosSalud() + "/" + jug.getVidaMaxima());
                inv.objetos.replace("Pocion", inv.objetos.get("Pocion") - 1);
                cantCombPoc.setText(inv.objetos.get("Pocion") + "");
                if (inv.objetos.get("Pocion") == 0) {
                    inv.objetos.remove("Pocion");
                }
            } else {
                cantCombPoc.setText("0");
                avisoCombatePociones.setText("No te quedan pociones");
            }
        } else if (en.getPuntosSalud() <= 0) {
            avisoTerminar.setText("El enemigo ha sido derrotado. Pulsa 'Terminar'");
        } else if (jug.getPuntosSalud() <= 0) {
            avisoTerminar.setText("Ya has sido derrotado. Pulsa 'Terminar'");
        }

        vidaCombJug.setText(jug.getPuntosSalud() + "/" + jug.getVidaMaxima());
    }//GEN-LAST:event_botonCombatePocionActionPerformed

